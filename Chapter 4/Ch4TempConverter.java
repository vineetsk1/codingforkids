public class Ch4TempConverter {
   public static void main(String[] args) {
   		double celsiusTemp = 60.0;
   		double farenheitTemp = 9.0*celsiusTemp/5.0 + 32; 
   		//uses conversion formula to calculate farenheit temperature
   		System.out.println("Temperature in Farenheit: " + farenheitTemp);	
   }

}